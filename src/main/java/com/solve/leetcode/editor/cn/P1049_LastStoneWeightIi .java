    public int lastStoneWeightII(int[] stones) {
        int sum = 0;
        for (int s : stones) sum += s;
        int n = sum / 2;
        boolean[] f = new boolean[sum];
        f[0] = true;
        for (int s : stones) {
            for (int j = n; j >= s; j--) {
                f[j] = f[j] || f[j-s];
            }
        }
        for (int i = n; i >= 0; i--) {
            if (f[i]) {
                return sum - 2*i;
            }
        }
        return 0;
    }
